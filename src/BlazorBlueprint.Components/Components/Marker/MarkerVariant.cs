namespace BlazorBlueprint.Components;

/// <summary>
/// Defines visual variants for the Marker component.
/// </summary>
public enum MarkerVariant
{
    /// <summary>
    /// Default inline marker for status or note content.
    /// </summary>
    Default,

    /// <summary>
    /// Marker with a bottom border used to separate rows.
    /// </summary>
    Border,

    /// <summary>
    /// Labeled separator marker with decorative lines.
    /// </summary>
    Separator
}
